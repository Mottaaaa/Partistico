package Model;

public class bosta {

    private String a;
    private String b;

    public bosta(String a, String b) {
        this.a = a;
        this.b = b;
    }

    public bosta() {
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getB() {
        return b;
    }

    public void setB(String b) {
        this.b = b;
    }
}
